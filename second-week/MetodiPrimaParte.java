public class MetodiPrimaParte {
    public static void main(String[] args) {
        returnChar('y');
        returnInt(10);
        returnDouble(10.0);
        returnString("Hello World!");
        returnBoolean(true);
    }

    static char returnChar(char A) {
        return A;
    }

    static int returnInt(int x) {
        return x;
    }

    static double returnDouble(double x) {
        return x;
    }

    static String returnString(String abc) {
        return abc;
    }

    static boolean returnBoolean(boolean arg) {
        return arg;
    }
}
