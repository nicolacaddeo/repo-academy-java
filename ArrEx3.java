public class ArrEx3 {
    public static void main(String[] args) {
        String[] cars = { "Volvo", "Ferrari", "Fiat" };
        int[] numbers = { 10, 20, 30 };

        for (int i = 0; i < cars.length; i++) {
            System.out.print(cars[i] + " ");
            System.out.print(numbers[i] + " ");
        }

    }
}
