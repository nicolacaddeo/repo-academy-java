public class SwitchEx {
    public static void main(String[] args) {
        int day = 4;

        switch (day) {
            case 1:
                System.out.println("Lunedi");
                break;
            case 2:
                System.out.println("Martedi");
                break;
            case 3:
                System.out.println("Mercoledi");
                break;
            case 4:
                System.out.println("Giovedi");
                break;
            case 5:
                System.out.println("Venerdi");
                break;
        }
    }
}
